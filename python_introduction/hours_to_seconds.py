
"""
converts a specific number of hours into seconds.

Remember, there are 3600 seconds in an hour
(since there are 60 minutes in an hour and 60 seconds in a minute, thus 60 x 60 = 3600).
"""
hours = 2
# seconds_in_hour = 3600

seconds = hours * 3600

print(f"{hours} hour(s) is {seconds} seconds.")